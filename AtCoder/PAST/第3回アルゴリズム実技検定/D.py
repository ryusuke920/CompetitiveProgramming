n = int(input())
s = [list(input()) for _ in range(5)]
number = [
    [".###", ".#.#", ".#.#", ".#.#", ".###"], 
    ["..#.", ".##.", "..#.", "..#.", ".###"], 
    [".###", "...#", ".###", ".#..", ".###"], 
    [".###", "...#", ".###", "...#", ".###"], 
    [".#.#", ".#.#", ".###", "...#", "...#"], 
    [".###", ".#..", ".###", "...#", ".###"], 
    [".###", ".#..", ".###", ".#.#", ".###"], 
    [".###", "...#", "...#", "...#", "...#"], 
    [".###", ".#.#", ".###", ".#.#", ".###"], 
    [".###", ".#.#", ".###", "...#", ".###"], 
]

ans = []
for i in range(n):
    bool = [True] * 10
    for h in range(5):
        for w in range(4):
            x = s[h][4 * i + w]
            for num in range(10):
                if x != number[num][h][w]:
                    bool[num] = False
    for j in range(10):
        if bool[j]:
            ans.append(j)
            break
print(*ans, sep="")