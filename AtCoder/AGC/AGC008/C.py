a, b, c, d, e, f, g = map(int, input().split())

ans = b * 2
# (a, d, e) = (1, 1, 1)で 6 マス
# (a, d, e) = (2, 0, 0)で 4 マス
# (a, d, e) = (0, 2, 0)で 4 マス
# (a, d, e) = (0, 0, 2)で 4 マス

# (a, d, e) = (2, 2, 2) 以上はそれぞれに分解できるので考える必要がない。これを１つ使うか１つも使わないかで貪欲する

# 1つ使う場合
if a >= 1 and d >= 1 and e >= 1:
    ans1 = 6 + ((a - 1) // 2 + (d - 1) // 2 + (e - 1) // 2) * 4
else:
    ans1 = 0

# １つも使わない場合
ans2 = (a // 2 + d // 2 + e // 2) * 4

print((ans + max(ans1, ans2)) // 2)